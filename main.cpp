#include "MorseTree.cpp"

int main(int argc, char* argv[]) {
	char cArray[58] = {'E', 'T', 'I', 'A', 'N', 'M', 'S', 'U', 'R', 'W', 
						'D', 'K', 'G', 'O', 'H', 'V', 'F', '\0', 'L', '\0', 
						'P', 'J', 'B', 'X', 'C', 'Y', 'Z', 'Q', '\0', '\0', 
						'5', '4', '3', '2', '1', '6', '7', '8', '9', '0',
						'\0', '\?', '\0', '\"', '+', '.', '\0', '@', '\'', 
						'-', '=', '/', '\0', ';', '!', '\0', ',', ':'};
	std::string sArray[58] = {".", "-", "..", ".-", "-.", "--", "...", "..-", ".-.", ".--", 
								"-..", "-.-", "--.", "---", "....", "...-", "..-.", "..--", ".-..", ".-.-", 
								".--.", ".---", "-...", "-..-", "-.-.", "-.--", "--..", "--.-", "---.", "----", 
								".....", "....-", "...--", "..---", ".----", "-....", "--...", "---..", "----.", "-----",
								"..--.", "..--..", ".-..-", ".-..-.", ".-.-.", ".-.-.-", ".--.-", ".--.-.", ".----.", 
								"-....-", "-...-", "-..-.", "-.-.-", "-.-.-.", "-.-.--", "--..-", "--..--", "---..."};
	
	MorseTree mTree;
	
	for (int i = 0; i < 58; i++) {
		mTree.addNode(cArray[i], sArray[i]);
	}
	
	mTree.printTree();
	std::string b = "The quick brown fox jumps over the yellow dog.";
	std::cout << b << "\n" << mTree.engToMorse("The quick brown fox jumps over the yellow dog.") << std::endl;
	
	return 0;
}
